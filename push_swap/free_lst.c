#include "pushswap.h"

void  free_lst(t_node **a)
{
	*a = (*a)->next;
	while(1)
	{
		if ((*a)->first == 1)
		{
			free(*a);
			break ;
		}
		free(*a);
		*a = (*a)->next;
	}
}
